#include "libft.h"

void	ft_putstr_fd(char const *s, int d)
{
	size_t i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			ft_putchar_fd(s[i], d);
			i++;
		}
	}
}
