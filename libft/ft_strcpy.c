#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t len;
	size_t i;

	i = 0;
	len = ft_strlen(src);
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
